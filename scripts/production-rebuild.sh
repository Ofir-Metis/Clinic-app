log() {
    local level=$1
    shift
    local message="$*"
    local timestamp=$(date '+%Y-%m-%d %H:%M:%S')
    
    case $level in
        "INFO")  echo -e "${CYAN}[${timestamp}] ℹ️  ${message}${NC}" | tee -a "$LOG_FILE" ;;
        "WARN")  echo -e "${YELLOW}[${timestamp}] ⚠️  ${message}${NC}" | tee -a "$LOG_FILE" ;;
        "ERROR") echo -e "${RED}[${timestamp}] ❌ ${message}${NC}" | tee -a "$LOG_FILE" ;;
        "SUCCESS") echo -e "${GREEN}[${timestamp}] ✅ ${message}${NC}" | tee -a "$LOG_FILE" ;;
        "STEP") echo -e "${PURPLE}[${timestamp}] 🚀 ${message}${NC}" | tee -a "$LOG_FILE" ;;
    esac
}

