main() {
    # Parse command line arguments
    while [[ $# -gt 0 ]]; do
        case $1 in
            --skip-tests)
                SKIP_TESTS=true
                shift
                ;;
            --keep-data)
                KEEP_DATA=true
                shift
                ;;
            --enhanced)
                ENHANCED_MODE=true
                COMPOSE_FILES+=("-f" "docker-compose.enhanced.yml")
                shift
                ;;
            --monitoring)
                MONITORING_MODE=true
                COMPOSE_FILES+=("-f" "docker-compose.enhanced.yml" "-f" "docker-compose.monitoring.yml")
                shift
                ;;
            --help|-h)
                show_help
                exit 0
                ;;
            *)
                log "ERROR" "Unknown option: $1"
                show_help
                exit 1
                ;;
        esac
    done
    
    # Show banner
    cat << EOF
🏗️ ===============================================
   CLINIC APP - AGGRESSIVE PRODUCTION REBUILD
   Complete Docker cleanup + fresh deployment
===============================================

⚠️  AGGRESSIVE CLEANUP MODE:
- ALL Docker containers will be stopped and removed
- ALL Docker images will be deleted (forces fresh build)
- Build cache will be cleared
- This ensures no cached layers interfere with deployment

Configuration:
- Keep Data: $KEEP_DATA
- Enhanced Mode: $ENHANCED_MODE
- Monitoring Mode: $MONITORING_MODE  
- Skip Tests: $SKIP_TESTS
- Log File: $LOG_FILE

EOF
    
    # Execute deployment steps
    check_prerequisites
    cleanup_environment  
    setup_environment_files
    install_dependencies
    build_docker_services
    start_infrastructure
    run_database_migrations
    start_application_services
    verify_service_health
    run_integration_tests
    generate_deployment_report
    
    # Success summary
    local end_time=$(date +%s)
    local duration=$((end_time - START_TIME))
    local duration_formatted=$(printf '%02d:%02d:%02d' $((duration/3600)) $((duration%3600/60)) $((duration%60)))
    
    cat << EOF

🎉 ===============================================
   DEPLOYMENT COMPLETED SUCCESSFULLY!
   Duration: $duration_formatted
