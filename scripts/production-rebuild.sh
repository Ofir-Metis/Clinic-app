check_prerequisites() {
    log "STEP" "Checking prerequisites..."
    
    # Check Node.js version
    if ! command -v node &> /dev/null; then
        log "ERROR" "Node.js not found. Please install Node.js 20+"
        exit 1
    fi
    
    local node_version=$(node -v | sed 's/v//' | cut -d. -f1)
    if [ "$node_version" -lt 20 ]; then
        log "ERROR" "Node.js version $node_version found. Please use Node.js 20+"
        exit 1
    fi
    
    # Check Yarn
    if ! command -v yarn &> /dev/null; then
        log "ERROR" "Yarn not found. Please install Yarn: npm install -g yarn"
        exit 1
    fi
    
    # Check Docker
    if ! command -v docker &> /dev/null; then
        log "ERROR" "Docker not found. Please install Docker Desktop"
        exit 1
    fi
    
    # Check Docker Compose
    if ! docker compose version &> /dev/null; then
        log "ERROR" "Docker Compose not found. Please install Docker Desktop with Compose"
        exit 1
    fi
    
    # Check Docker is running
    if ! docker info &> /dev/null; then
        log "ERROR" "Docker is not running. Please start Docker Desktop"
        exit 1
    fi
    
    log "SUCCESS" "All prerequisites satisfied"
}

cleanup_environment() {
    log "STEP" "Performing aggressive Docker cleanup..."
    
    cd "$PROJECT_ROOT"
    
    # Stop all running containers gracefully
    log "INFO" "Stopping all running containers..."
    if docker compose "${COMPOSE_FILES[@]}" ps -q | grep -q .; then
        docker compose "${COMPOSE_FILES[@]}" down --timeout 30 || true
    fi
    
    # Stop additional compose files if they exist
    if [ -f "docker-compose.enhanced.yml" ]; then
        docker compose -f docker-compose.yml -f docker-compose.enhanced.yml down --timeout 30 || true
    fi
    
    if [ -f "docker-compose.monitoring.yml" ]; then
        docker compose -f docker-compose.yml -f docker-compose.enhanced.yml -f docker-compose.monitoring.yml down --timeout 30 || true
    fi
    
    # Remove orphaned containers
    log "INFO" "Removing orphaned containers..."
    docker compose "${COMPOSE_FILES[@]}" down --remove-orphans || true
    
    # AGGRESSIVE CLEANUP: Stop and remove ALL containers
    log "INFO" "Performing aggressive container cleanup..."
    if [ "$(docker ps -aq)" ]; then
        log "INFO" "Stopping all running containers..."
        docker stop $(docker ps -aq) 2>/dev/null || true
        
        log "INFO" "Removing all containers..."
        docker rm -f $(docker ps -aq) 2>/dev/null || true
    fi
    
    # AGGRESSIVE CLEANUP: Remove ALL images (forces complete rebuild)
    log "INFO" "Performing aggressive image cleanup..."
    if [ "$(docker images -aq)" ]; then
        log "WARN" "Removing ALL Docker images to force complete rebuild..."
        docker rmi -f $(docker images -aq) 2>/dev/null || true
    fi
    
    # Clean up build cache
    log "INFO" "Cleaning Docker build cache..."
    docker builder prune -af || true
    
    # Clean up system (everything except volumes if keeping data)
    log "INFO" "Cleaning Docker system..."
    if [ "$KEEP_DATA" = false ]; then
        log "INFO" "Cleaning up ALL Docker volumes..."
        docker system prune -af --volumes || true
    else
        log "INFO" "Cleaning Docker system (preserving volumes)..."
        docker system prune -af || true
    fi
    
    # Clean up specific clinic-app volumes only if not keeping data
    if [ "$KEEP_DATA" = false ]; then
        if [ "$(docker volume ls --filter "name=clinic-app" -q)" ]; then
            log "INFO" "Cleaning up clinic-app specific volumes..."
            docker volume ls --filter "name=clinic-app" -q | xargs -r docker volume rm || true
        fi
    fi
    
    # Clean up networks
    log "INFO" "Cleaning up Docker networks..."
    # Remove all custom networks (except default ones)
    if [ "$(docker network ls --filter type=custom -q)" ]; then
        docker network ls --filter type=custom -q | xargs -r docker network rm 2>/dev/null || true
    fi
    
    # Verify cleanup
    local remaining_containers=$(docker ps -aq | wc -l)
    local remaining_images=$(docker images -aq | wc -l)
    
    log "INFO" "Cleanup summary:"
    log "INFO" "  - Remaining containers: $remaining_containers"
    log "INFO" "  - Remaining images: $remaining_images"
    
    if [ "$remaining_containers" -eq 0 ] && [ "$remaining_images" -eq 0 ]; then
        log "SUCCESS" "Complete Docker cleanup successful - all images and containers removed"
    elif [ "$remaining_images" -eq 0 ]; then
        log "SUCCESS" "All Docker images removed successfully"
    else
        log "WARN" "Some Docker resources remain (may include system/protected resources)"
    fi
}

setup_environment_files() {
    log "STEP" "Setting up environment files..."
    
    cd "$PROJECT_ROOT"
    
    # Check if .env exists, if not copy from example
    if [ ! -f ".env" ]; then
        if [ -f ".env.example" ]; then
            cp ".env.example" ".env"
            log "INFO" "Created .env from .env.example"
        else
            log "WARN" "No .env.example found, creating minimal .env"
            cat > .env << EOF
NODE_ENV=production
POSTGRES_HOST=localhost
POSTGRES_PORT=5432
POSTGRES_DB=clinic
POSTGRES_USER=postgres
POSTGRES_PASSWORD=postgres
JWT_SECRET=clinic_jwt_production_ultra_secure_secret_key_2024_very_long
NATS_URL=nats://localhost:4222
REDIS_URL=redis://localhost:6379
API_GATEWAY_PORT=4000
EOF
        fi
    else
        log "INFO" "Using existing .env file"
    fi
    
    # Ensure production environment
    if grep -q "NODE_ENV=development" .env; then
        sed -i 's/NODE_ENV=development/NODE_ENV=production/g' .env
        log "INFO" "Updated NODE_ENV to production"
    fi
    
    log "SUCCESS" "Environment files configured"
}

install_dependencies() {
    log "STEP" "Installing dependencies..."
    
    cd "$PROJECT_ROOT"
    
    # Clean yarn cache
    log "INFO" "Cleaning Yarn cache..."
    yarn cache clean
    
    # Install root dependencies (allow lockfile updates for local production rebuild)
    log "INFO" "Installing root dependencies..."
    yarn install --production=false
    
    # Build common library first (critical dependency)
    log "INFO" "Building @clinic/common library..."
    yarn workspace @clinic/common build
    
    # Verify common library build
    if [ ! -d "libs/common/dist" ]; then
        log "ERROR" "@clinic/common build failed - dist directory not found"
        exit 1
    fi
    
    # Install and build frontend dependencies
    log "INFO" "Installing frontend dependencies..."
    cd "${PROJECT_ROOT}/frontend"
    yarn install
    
    log "SUCCESS" "Dependencies installed and built"
}

build_docker_services() {
    log "STEP" "Building Docker services..."
    
    cd "$PROJECT_ROOT"
    
    # Build all services with no cache for production-ready build
    log "INFO" "Building Docker images (this may take several minutes)..."
    
    # Core services
    local core_services=(
        "api-gateway"
        "auth-service" 
        "appointments-service"
        "files-service"
        "notifications-service"
        "notes-service"
        "analytics-service"
        "settings-service"
        "billing-service"
        "therapists-service"
        "google-integration-service"
        "client-relationships-service"
    )
    
    # Build core services in parallel groups to avoid resource exhaustion
    local build_batch_size=4
    for ((i=0; i<${#core_services[@]}; i+=build_batch_size)); do
        local batch=("${core_services[@]:i:build_batch_size}")
        log "INFO" "Building batch: ${batch[*]}"
        
        for service in "${batch[@]}"; do
            docker compose "${COMPOSE_FILES[@]}" build --no-cache "$service" &
        done
        wait  # Wait for batch to complete
    done
    
    # Build enhanced services if requested
    if [ "$ENHANCED_MODE" = true ]; then
        log "INFO" "Building enhanced services..."
        local enhanced_services=("ai-service" "search-service" "cdn-service" "progress-service")
        for service in "${enhanced_services[@]}"; do
            docker compose -f docker-compose.yml -f docker-compose.enhanced.yml build --no-cache "$service" &
        done
        wait
    fi
    
    log "SUCCESS" "Docker services built successfully"
}

start_infrastructure() {
    log "STEP" "Starting infrastructure services..."
    
    cd "$PROJECT_ROOT"
    
    # Start infrastructure services first
    local infrastructure_services=(
        "postgres"
        "redis" 
        "nats"
        "minio"
        "maildev"
    )
    
    if [ "$ENHANCED_MODE" = true ]; then
        infrastructure_services+=("elasticsearch")
        # Create external network if it doesn't exist (required for enhanced compose files)
        log "INFO" "Creating clinic-network if it doesn't exist..."
        docker network create clinic-network 2>/dev/null || log "INFO" "clinic-network already exists"
    fi
    
    log "INFO" "Starting infrastructure: ${infrastructure_services[*]}"
    docker compose "${COMPOSE_FILES[@]}" up -d "${infrastructure_services[@]}"
    
    # Wait for infrastructure to be ready
    log "INFO" "Waiting for infrastructure services to be ready..."
    
    # Wait for PostgreSQL
    local max_attempts=30
    local attempt=1
    while ! docker compose "${COMPOSE_FILES[@]}" exec -T postgres pg_isready -U postgres &> /dev/null; do
        if [ $attempt -ge $max_attempts ]; then
            log "ERROR" "PostgreSQL failed to start after $max_attempts attempts"
            exit 1
        fi
        log "INFO" "Waiting for PostgreSQL... (attempt $attempt/$max_attempts)"
        sleep 2
        ((attempt++))
    done
    
    # Wait for Redis
    attempt=1
    while ! docker compose "${COMPOSE_FILES[@]}" exec -T redis redis-cli ping &> /dev/null; do
        if [ $attempt -ge $max_attempts ]; then
            log "ERROR" "Redis failed to start after $max_attempts attempts"
            exit 1
        fi
        log "INFO" "Waiting for Redis... (attempt $attempt/$max_attempts)"
        sleep 2
        ((attempt++))
    done
    
    # Wait for NATS (check container status instead of nc which may not be available)
    attempt=1
    while ! docker compose "${COMPOSE_FILES[@]}" ps nats | grep -q "Up" &> /dev/null; do
        if [ $attempt -ge $max_attempts ]; then
            log "ERROR" "NATS failed to start after $max_attempts attempts"
            exit 1
        fi
        log "INFO" "Waiting for NATS... (attempt $attempt/$max_attempts)"
        sleep 2
        ((attempt++))
    done
    
    log "SUCCESS" "Infrastructure services started and ready"
}

run_database_migrations() {
    if [ "$KEEP_DATA" = true ]; then
        log "INFO" "Skipping database migrations (--keep-data flag set)"
        return 0
    fi
    
    log "STEP" "Running database migrations..."
    
    cd "$PROJECT_ROOT"
    
    # Drop and recreate database for fresh start
    log "INFO" "Resetting database for fresh deployment..."
    docker compose "${COMPOSE_FILES[@]}" exec -T postgres psql -U postgres -c "DROP DATABASE IF EXISTS clinic;" || true
    docker compose "${COMPOSE_FILES[@]}" exec -T postgres psql -U postgres -c "CREATE DATABASE clinic;" || true
    
    # Wait a moment for database to be ready
    sleep 2
    
    # Run migrations for each service that has them
    local services_with_migrations=(
        "api-gateway"
        "auth-service"
        "appointments-service"
        "ai-service"
        "google-integration-service"
    )
    
    for service in "${services_with_migrations[@]}"; do
        if [ -d "services/$service/src/migrations" ]; then
            log "INFO" "Running migrations for $service..."
            # Note: This would need to be adapted based on how each service handles migrations
            # For now, we'll let TypeORM auto-sync handle it in development
        fi
    done
    
    log "SUCCESS" "Database migrations completed"
}

start_application_services() {
    log "STEP" "Starting application services..."
    
    cd "$PROJECT_ROOT"
    
    # Start core services in dependency order
    local service_groups=(
        # Group 1: Independent services
        "auth-service files-service settings-service"
        # Group 2: Services that depend on auth
        "appointments-service notes-service notifications-service analytics-service"
        # Group 3: Services that depend on others
        "billing-service therapists-service google-integration-service client-relationships-service"
        # Group 4: API Gateway (depends on all)
        "api-gateway"
        # Group 5: Frontend and load balancer
        "frontend nginx"
    )
    
    for group in "${service_groups[@]}"; do
        log "INFO" "Starting service group: $group"
        docker compose "${COMPOSE_FILES[@]}" up -d $group
        
        # Wait between groups for proper startup
        sleep 10
    done
    
    # Start enhanced services if requested
    if [ "$ENHANCED_MODE" = true ]; then
        log "INFO" "Starting enhanced services..."
        docker compose -f docker-compose.yml -f docker-compose.enhanced.yml up -d \
            ai-service search-service cdn-service progress-service
    fi
    
    # Start monitoring services if requested
    if [ "$MONITORING_MODE" = true ]; then
        log "INFO" "Starting monitoring services..."
        docker compose -f docker-compose.yml -f docker-compose.enhanced.yml -f docker-compose.monitoring.yml up -d \
            prometheus grafana loki jaeger uptime-kuma falco
    fi
    
    log "SUCCESS" "Application services started"
}

verify_service_health() {
    log "STEP" "Verifying service health..."
    
    # Define core services and their health endpoints
    local -A health_endpoints=(
        ["api-gateway"]="http://localhost:4000/health"
        ["auth-service"]="http://localhost:3001/health"  
        ["appointments-service"]="http://localhost:3002/health"
["files-service"]="http://localhost:3003/health"
        ["notifications-service"]="http://localhost:3004/health"
        ["notes-service"]="http://localhost:3006/health"
        ["analytics-service"]="http://localhost:3007/health"
        ["settings-service"]="http://localhost:3008/health"
        ["billing-service"]="http://localhost:3009/health"
        ["therapists-service"]="http://localhost:3013/health"
        ["google-integration-service"]="http://localhost:3012/health"
        ["client-relationships-service"]="http://localhost:3014/health"
    )
    
    local failed_services=()
    local max_attempts=20
    
    # Wait for services to start up
    log "INFO" "Waiting for services to start (this may take a few minutes)..."
    sleep 30
    
    for service in "${!health_endpoints[@]}"; do
        local endpoint="${health_endpoints[$service]}"
        local attempt=1
        local service_ready=false
        
        while [ $attempt -le $max_attempts ]; do
            if curl -sf "$endpoint" &> /dev/null; then
                log "SUCCESS" "$service health check passed"
                service_ready=true
                break
            fi
            
            log "INFO" "Waiting for $service... (attempt $attempt/$max_attempts)"
            sleep 3
            ((attempt++))
        done
        
        if [ "$service_ready" = false ]; then
            log "ERROR" "$service health check failed after $max_attempts attempts"
            failed_services+=("$service")
        fi
    done
    
    if [ ${#failed_services[@]} -eq 0 ]; then
        log "SUCCESS" "All core services are healthy"
    else
        log "ERROR" "The following services failed health checks: ${failed_services[*]}"
        log "INFO" "Check service logs with: docker compose logs <service-name>"
        return 1
    fi
}

run_integration_tests() {
    if [ "$SKIP_TESTS" = true ]; then
        log "INFO" "Skipping tests (--skip-tests flag set)"
        return 0
    fi
    
    log "STEP" "Running integration tests..."
    
    cd "$PROJECT_ROOT"
    
    # Wait a bit more for services to fully stabilize
    log "INFO" "Allowing services to stabilize before testing..."
    sleep 15
    
    # Run E2E tests
    if [ -f "scripts/test-e2e.sh" ]; then
        log "INFO" "Running Playwright E2E tests..."
        if ./scripts/test-e2e.sh --browser chromium --headed=false; then
            log "SUCCESS" "E2E tests passed"
        else
            log "WARN" "Some E2E tests failed, but deployment continues"
        fi
    else
        log "WARN" "E2E test script not found, skipping tests"
    fi
    
    # Test basic API endpoints
    log "INFO" "Testing basic API endpoints..."
    local test_endpoints=(
        "http://localhost:4000/health"
        "http://localhost:4000/api/auth/health" 
        "http://localhost:5173"  # Frontend
    )
    
    for endpoint in "${test_endpoints[@]}"; do
        if curl -sf "$endpoint" &> /dev/null; then
            log "SUCCESS" "API endpoint test passed: $endpoint"
        else
            log "WARN" "API endpoint test failed: $endpoint"
        fi
    done
}

